<?php
/**
 * conn.php - ไฟล์สำหรับสร้างการเชื่อมต่อฐานข้อมูล
 * ระบบจัดการการลางานพนักงานรีสอร์ทลาลามูก้าเต็นท์เขาใหญ่
 */

// ข้อมูลการเชื่อมต่อฐานข้อมูล (ปรับเปลี่ยนค่าเหล่านี้ตามการตั้งค่าของคุณ)
$servername = "localhost"; // หรือ IP Address ของ Server
$username = "root";       // ชื่อผู้ใช้ฐานข้อมูล
$password = "";           // รหัสผ่านฐานข้อมูล (ควรกำหนดรหัสผ่านที่รัดกุมในการใช้งานจริง)
$dbname = "hotel"; // ชื่อฐานข้อมูลที่คุณสร้างไว้

// สร้างการเชื่อมต่อฐานข้อมูล (ใช้ MySQLi Object-Oriented Style)
$conn = new mysqli($servername, $username, $password, $dbname);

// ตรวจสอบการเชื่อมต่อ
if ($conn->connect_error) {
    // ถ้าเชื่อมต่อไม่สำเร็จ ให้แสดงข้อความแจ้งเตือนและหยุดการทำงาน
    die("❌ การเชื่อมต่อฐานข้อมูลล้มเหลว: " . $conn->connect_error);
}

// ตั้งค่า Charset ให้เป็น UTF-8 เพื่อรองรับภาษาไทย
$conn->set_charset("utf8");

// การใช้งาน: คุณสามารถเรียกใช้ไฟล์นี้ในทุกหน้า PHP ที่ต้องการใช้งานฐานข้อมูล
// เช่น: require_once 'conn.php';
// จากนั้นสามารถใช้ตัวแปร $conn เพื่อเรียกใช้ Query ต่อไปได้

// echo "✅ การเชื่อมต่อฐานข้อมูลสำเร็จ!"; 

?>